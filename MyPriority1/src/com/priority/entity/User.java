package com.priority.entity;

public class User {
	
	private int id;
	private String name;
	private float connection;
	private float relationship;
	private float career;
	private float wealth;
	
	//getter and setter method
	public long getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getConnection() {
		return connection;
	}
	public void setConnection(float connection) {
		this.connection = connection;
	}
	public float getRelationship() {
		return relationship;
	}
	public void setRelationship(float relationship) {
		this.relationship = relationship;
	}
	public float getCareer() {
		return career;
	}
	public void setCareer(float career) {
		this.career = career;
	}
	public float getWealth() {
		return wealth;
	}
	public void setWealth(float wealth) {
		this.wealth = wealth;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
